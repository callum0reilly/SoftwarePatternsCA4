public class Product {
    private String title;
    private double price;

    public Product(String title, double price) {
        this.title = title;
        this.price = price;
    }//end of constructore

    public String getTitle() {
        return title;
    }//end of getTitle

    public double getPrice() {
        return price;
    }//end of getPrice
}//end of class